    private void loadEmployees() {
    try {
        String sql = "SELECT * FROM Employee";
        pstmt = con_obj.prepareStatement(sql);
        res = pstmt.executeQuery();

        DefaultTableModel model = (DefaultTableModel) tblEmployees.getModel();
        model.setRowCount(0); // Clear table rows

        while (res.next()) {
            model.addRow(new Object[]{
                res.getInt("Employee_ID"),
                res.getString("E_Name"),
                res.getString("Username"),
                res.getString("Role")
            });
        }

        res.close();
        pstmt.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading employees.");
    }
}
    
    private Integer getSelectedEmployeeId() {
    int selectedRow = tblEmployees.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select an employee.");
        return null;
    }
    return (int) tblEmployees.getValueAt(selectedRow, 0);
}


