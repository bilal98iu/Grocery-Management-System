    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        try {
        String sql = "INSERT INTO Employee (E_Name, Username, Password, Role) VALUES (?, ?, ?, ?)";
        PreparedStatement pst = con_obj.prepareStatement(sql);
        pst.setString(1, txtName.getText());
        pst.setString(2, txtUsername.getText());
        pst.setString(3, txtPassword.getText());
        pst.setString(4, "Cashier"); // Only Cashiers added from here

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Employee added successfully!");

        loadEmployees(); // Refresh table
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Failed to add employee.");
    }
    }//GEN-LAST:event_btnaddActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        Integer id = getSelectedEmployeeId();
    if (id == null) return;

    try {
        String sql = "UPDATE Employee SET E_Name = ?, Username = ?, Password = ? WHERE Employee_ID = ?";
        PreparedStatement pst = con_obj.prepareStatement(sql);
        pst.setString(1, txtName.getText());
        pst.setString(2, txtUsername.getText());
        pst.setString(3, txtPassword.getText());
        pst.setInt(4, id);

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Employee updated!");
        loadEmployees();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Failed to update employee.");
    }
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        Integer id = getSelectedEmployeeId();
    if (id == null) return;

    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete?", "Confirm", JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) return;

    try {
        String sql = "DELETE FROM Employee WHERE Employee_ID = ?";
        PreparedStatement pst = con_obj.prepareStatement(sql);
        pst.setInt(1, id);

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Employee deleted!");
        loadEmployees();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Failed to delete employee.");
    }
    }//GEN-LAST:event_btndeleteActionPerformed

